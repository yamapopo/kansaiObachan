function osakaDecode(text)
{
	var s;
	
	for (key in tbl) {
		s = key;
		s = s.replace(/\./g,"\\.");
		s = s.replace(/\?/g,"\\?");
		s = s.replace(/\)/g,"\\)");
		s = s.replace(/\(/g,"\\(");
		s = s.replace(/\+/g,"\\+");
		s = s.replace(/\!/g,"\\!");
		s = s.replace(/\[/g,"\\[");
		s = s.replace(/\]/g,"\\]");
		s = s.replace(/\^/g,"\\^");
		s = s.replace(/\$/g,"\\$");
		text = text.replace(new RegExp(eval('/'+ s+ '/g')), tbl[key]);
	}
	return text;
}

	
